let fruta = "manzana";

switch (fruta) {
    case "manzana":
        console.log("Es una manzana");
        break;
    case "banana":
        console.log("Es una banana");
        break;
    case "pera":
        console.log("Es una pera");
        break;
    default:
        console.log("Fruta no reconocida");
}

